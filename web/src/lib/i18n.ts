export function useI18n() {
  const { language } = useAppSettings();

  return useMemo(() => {
    const activeLanguage = (language in TRANSLATIONS ? language : "en") as SupportedLanguage;
    const dictionary = TRANSLATIONS[activeLanguage] || TRANSLATIONS.en;

    return {
      language: activeLanguage,
      t: (key: string, fallback?: string) => dictionary[key] || TRANSLATIONS.en[key] || fallback || key,
    };
  }, [language]);
}
